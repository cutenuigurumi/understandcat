#include<stdio.h>

int main()
{
    int sum = 0, p = 0, n = 0;

    while(p != -1){
        printf("点数(-1で終了)----");
        scanf("%d", &p);
        if(p != -1) {
            sum = sum + p;
            n++;
        }
    }
    if(n != 0) {
        printf("%d人の合計点は%dです。\n", n, sum);
        printf("平均点は%5.1f点です\n", (double)sum /n);
    }
    return 0;
}
